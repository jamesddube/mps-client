    @SuppressWarnings("StatementWithEmptyBody")
    @Override
    public boolean onNavigationItemSelected(MenuItem item) {
        // Handle navigation view item clicks here.
        int id = item.getItemId();
        FragmentManager fragmentManager = getSupportFragmentManager();

        if (id == R.id.nav_dashboard) {
            // Handle the camera action
            fragmentManager.beginTransaction()
                    .replace(R.id.fragment_container, Fragment_Dashboard.newInstance())
                    .commit();

        } else if (id == R.id.nav_gallery) {
            fragmentManager.beginTransaction()
                    .replace(R.id.fragment_container, Fragment_Orders.newInstance())
                    .commit();

        } else if (id == R.id.nav_customers) {
            fragmentManager.beginTransaction()
                    .replace(R.id.fragment_container, Fragment_Customers.newInstance())
                    .commit();

        } else if (id == R.id.nav_sync) {

            Intent i = new Intent("android.intent.action.SYNC");
            startActivity(i);

        } else if (id == R.id.nav_settings) {

        } else if (id == R.id.nav_send) {

        }

        DrawerLayout drawer = (DrawerLayout) findViewById(R.id.drawer_layout);
        drawer.closeDrawer(GravityCompat.START);
        return true;
    }

